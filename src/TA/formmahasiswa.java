    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       DefaultTableModel tabelTampil1 = new DefaultTableModel();
tabelTampil1.addColumn("name");
tabelTampil1.addColumn("nim");
tabelTampil1.addColumn("kelas");
tabelTampil1.addColumn("matakuliah");
tabelTampil1.addColumn("tgl");

try {
    Connection c = ClassDatabase.getKoneksi();
    Statement s = c.createStatement();
    String sql = "SELECT * FROM mhs WHERE nim LIKE '%" + txtCaridata.getText() +
                 "%' OR name LIKE '%" + txtCaridata.getText() +
                 "%' OR kelas LIKE '%" + txtCaridata.getText() +
                 "%' OR matakuliah LIKE '%" + txtCaridata.getText() +
                 "%' OR tgl LIKE '%" + txtCaridata.getText() + "%'";
    ResultSet r = s.executeQuery(sql);
    
    while (r.next()) {
        tabelTampil1.addRow(new Object[]{
            r.getString("name"),
            r.getString("nim"),
            r.getString("kelas"),
            r.getString("matakuliah"),
            r.getString("tgl")
        });
    }
    
    tblmahasiswa.setModel(tabelTampil1);
} catch (SQLException e) {
    e.printStackTrace();
}
    }//GEN-LAST:event_jButton4ActionPerformed

