    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
try {
        Connection con = ClassDatabase.getkoneksi();
        String insertQuery = "INSERT INTO mhs (nama, nim, kelas, matakuliah, kehadiran) VALUES (?, ?, ?, ?)";
        try (java.sql.PreparedStatement preparedStatement = con.prepareStatement(insertQuery)) {
            preparedStatement.setString(1, t_nama.getText());
            preparedStatement.setString(2, t_nim.getText());
            preparedStatement.setString(3, t_kelas.getText());
            preparedStatement.setString(4, t_matakuliah.getText());
            preparedStatement.setString(5, t_kehadiran.getText());
            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Anda Berhasil Daftar", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            // Setelah memasukkan data, panggil metode untuk menampilkan data di tabel
            displayDataInTable();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Registration failed. Error: " + e.getMessage(), "Registration Error", JOptionPane.ERROR_MESSAGE);
    }

    }                                      

    private void displayDataInTable() {


    try {
        Connection con = ClassDatabase.getkoneksi();
        Statement sta = con.createStatement();
        ResultSet re = sta.executeQuery("Select * from mhs");

        while (re.next()) {
            Object[] absensi = new Object[5];
            absensi[0] = re.getString("nama");
            absensi[1] = re.getString("nim");
            absensi[2] = re.getString("kelas");
            absensi[3] = re.getString("matakuliah");
            absensi[3] = re.getString("kahadiran");
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Tidak Tampil Ke Tabel");
        System.out.println("Error: " + e.getMessage());
    }//GEN-LAST:event_btnSimpanActionPerformed
    }
    
